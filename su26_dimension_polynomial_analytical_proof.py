def binomial_representation(k: int) -> int:
    """
    БИНОМИАЛЬНОЕ ПРЕДСТАВЛЕНИЕ:
    n(k) = 206·C(k,4) + 124·C(k,3) − 48·C(k,2) + 26·C(k,1)
    """
    C = lambda n, m: math.comb(n, m) if n >= m >= 0 else 0
    return 206 * C(k, 4) + 124 * C(k, 3) - 48 * C(k, 2) + 26 * C(k, 1)


def verify_binomial(k_max: int = 15) -> List[Dict]:
    """ПРОВЕРКА БИНОМИАЛЬНОГО ПРЕДСТАВЛЕНИЯ."""
    results = []
    for k in range(1, k_max + 1):
        n1, n2 = n_poly(k), binomial_representation(k)
        results.append({'k': k, 'n(k)': n1, 'binomial': n2, 'ok': n1 == n2})
    return results


