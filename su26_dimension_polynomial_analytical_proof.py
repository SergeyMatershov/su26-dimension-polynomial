def euler_phi(n: int) -> int:
    """ФУНКЦИЯ ЭЙЛЕРА φ(n) — количество чисел ≤ n, взаимно простых с n."""
    result = n
    p = 2
    while p * p <= n:
        if n % p == 0:
            while n % p == 0:
                n //= p
            result -= result // p
        p += 1 if p == 2 else 2
    if n > 1:
        result -= result // n
    return result


def zeta3_approx(num_primes: int = 1000) -> float:
    """
    ПРИБЛИЖЕНИЕ ζ(3) ЧЕРЕЗ ПРОИЗВЕДЕНИЕ ЭЙЛЕРА.
    ζ(3) = ∏_{p prime} (1 − 1/p³)⁻¹
    Точное значение: 1.2020569031595942 (константа Апери)
    """
    sieve = bytearray(b'\x01') * (num_primes + 1)
    sieve[0:2] = b'\x00\x00'
    for i in range(2, int(math.isqrt(num_primes)) + 1):
        if sieve[i]:
            sieve[i*i:num_primes+1:i] = b'\x00' * ((num_primes - i*i) // i + 1)
    
    product = 1.0
    for p in range(2, num_primes + 1):
        if sieve[p]:
            product /= (1.0 - 1.0 / p**3)
    return product


