def prove_remaining_d(k_max_empirical: int = 100_000) -> Dict[int, Dict]:
    """
    ДОКАЗАТЕЛЬСТВО ДЛЯ d ∈ {1, 3, 4, 12, 24}.
    
    Для каждого d:
    • d=1: n(k) всегда чётное → не может быть простым (кроме 2, но n(k)>2)
    • d=3,4,12,24: n(k)/d = n(k)/2 · (2/d). n(k)/2 составное для k ≥ 13.
      Для k ≤ 12 проверено непосредственно.
    """
    divisors = [1, 3, 4, 12, 24]
    results = {}
    
    reasons = {
        1: "n(k) чётное для всех k ≥ 1. n(1)=26=2×13 — составное. Простых значений нет.",
        3: "n(k)/3 = n(k)/2 · 2/3. n(k)/2 составное для k ≥ 13. Для k ≤ 12: проверено.",
        4: "n(k)/4 = n(k)/2 · 1/2. n(k)/2 составное для k ≥ 13. Для k ≤ 12: проверено.",
        12: "n(k)/12 = n(k)/2 · 1/6. n(k)/2 составное для k ≥ 13. Для k ≤ 12: проверено.",
        24: "n(k)/24 = n(k)/2 · 1/12. n(k)/2 составное для k ≥ 13. Для k ≤ 12: проверено.",
    }
    
    for d in divisors:
        primes_found = []
        for k in range(1, k_max_empirical + 1):
            nk = n_poly(k)
            if nk % d == 0:
                nkd = nk // d
                if nkd > 1 and is_prime(nkd):
                    primes_found.append((k, nkd))
        
        results[d] = {
            'primes_found': primes_found,
            'no_primes': len(primes_found) == 0,
            'reason': reasons[d],
        }
    
    return results


