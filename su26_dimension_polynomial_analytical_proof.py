def prove_n_over_2(k_max_empirical: int = 1_000_000) -> Dict:
    """
    ПОЛНОЕ ДОКАЗАТЕЛЬСТВО ДЛЯ n(k)/2.
    
    ТЕОРЕМА: n(k)/2 составное для всех k ≥ 13.
    Простые значения: k ∈ {1, 2, 3, 6, 12}.
    
    ДОКАЗАТЕЛЬСТВО:
    1. 10 «хороших» классов: n(k)/2 = k · M(k).
       При k ≥ 13: k ≥ 13 > 1, M(k) ≥ M(13) = 6898 > 1.
    2. 14 «плохих» классов: n(k)/2 = (k/d) · f(k)/g.
       При k ≥ 13: k/d ≥ 13/d ≥ 13/12 > 1 (максимальное d=12).
       f(k)/g ≥ f(13)/12 = 6898.17... > 1.
    3. Проверка k = 1..12: простые при k = 1, 2, 3, 6, 12.
    """
    classes = classify_mod24_classes()
    
    # Находим максимальную границу
    max_threshold = max(c['threshold'] for c in classes.values())
    
    # Проверяем факторизацию для всех классов при k ≥ threshold
    all_verified = True
    for r in range(24):
        c = classes[r]
        # Находим первое k в этом классе, ≥ threshold
        k_test = r if r > 0 else 24
        while k_test < c['threshold']:
            k_test += 24
        
        nk2 = n_poly_over_2(k_test)
        
        if c['type'] == "GOOD":
            f1, f2 = k_test, M_exact(k_test)
        else:
            f1, f2 = k_test // c['d'], f_poly(k_test) // c['gcd']
        
        if f1 * f2 != nk2 or f1 <= 1 or f2 <= 1:
            all_verified = False
    
    # Эмпирическая проверка
    primes_found = []
    for k in range(1, k_max_empirical + 1):
        nk2 = n_poly_over_2(k)
        if is_prime(nk2):
            primes_found.append((k, nk2))
    
    return {
        'max_threshold': max_threshold,
        'all_verified': all_verified,
        'primes_found': primes_found,
        'primes_match': set(p[0] for p in primes_found) == {1, 2, 3, 6, 12},
        'k_max_empirical': k_max_empirical,
    }


