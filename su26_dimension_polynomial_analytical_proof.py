def n_poly(k: int) -> int:
    """
    АКСИОМАТИЧЕСКОЕ ОПРЕДЕЛЕНИЕ ПОЛИНОМА:
    n(k) = (103k⁴ − 370k³ + 101k² + 478k) / 12
    
    Полином является САМОСТОЯТЕЛЬНЫМ математическим объектом,
    изучаемым наравне с другими полиномами (Бернулли, Эйлера и т.д.).
    Никакая формула Вейля или C(k+25,25) НЕ используется в определении.
    
    МОТИВИРОВКА (из Σ-Ω⁴ теории):
    Полином порождает размерности калибровочных групп в иерархии:
    n(1)=26 → SU(26), n(2)=4 → SU(4), n(3)=58 → SU(58), n(4)=518 → SU(518).
    Разбиения значений дают структуру подгрупп:
    58=26+32 → SU(58)⊃SU(26)×SU(32)×U(1), 26=6+20 → SU(26)⊃SU(6)×SU(20).
    
    Коэффициенты выражены через числа Бетти BSU(n):
    103 = 6β₁₂² + β₀β₁₀ + β₀β₁₂ + β₀
    370 = 23β₁₂² + β₁₀
    101 = 6β₁₂² + β₀ + β₀β₁₂
    478 = 29β₁₂² + β₁₀β₁₂ + β₀β₁₀ + β₀β₁₂
    где β₀=1, β₁₀=2, β₁₂=4.
    
    КЛЮЧЕВЫЕ ЗНАЧЕНИЯ:
    k = -4 → n(-4) = 4146 = 2 × 3 × 691  (связь с Рамануджаном)
    k = -1 → n(-1) = 8                    (предгеометрия)
    k =  0 → n(0) = 0                     (тривиальное представление)
    k =  1 → n(1) = 26                    (SU(26) — видимый сектор)
    k =  2 → n(2) = 4                     (SU(4) ≅ SO(6) — Пати-Салам)
    k =  3 → n(3) = 58                    (SU(58) — унификация)
    k =  4 → n(4) = 518                   (SU(518) — χ-резонанс)
    k =  5 → n(5) = 1920                  (SU(1920) — Планковский масштаб)
    """
    return (103 * k**4 - 370 * k**3 + 101 * k**2 + 478 * k) // 12


def f_poly(k: int) -> int:
    """
    ВСПОМОГАТЕЛЬНЫЙ КУБИЧЕСКИЙ ПОЛИНОМ: f(k) = 103k³ − 370k² + 101k + 478
    
    Через него выражается факторизация n(k):
    n(k) = k · f(k) / 12
    n(k)/2 = k · f(k) / 24
    
    Значения f(k) mod 24 определяют структуру доказательства:
    • f(k) ≡ 0 (mod 24) → M(k) = f(k)/24 целое → «хороший» класс
    • f(k) ≢ 0 (mod 24) → M(k) не целое → «плохой» класс
    """
    return 103 * k**3 - 370 * k**2 + 101 * k + 478


def M_exact(k: int) -> int:
    """
    M(k) = f(k) / 24 — целое ТОЛЬКО для «хороших» классов mod 24.
    Для этих классов: n(k)/2 = k · M(k) — непосредственная факторизация.
    """
    return f_poly(k) // 24


def n_poly_over_2(k: int) -> int:
    """ПРИВЕДЁННОЕ ЗНАЧЕНИЕ: n(k)/2. Всегда целое для k ≥ 1."""
    return n_poly(k) // 2


def factorize_str(n: int) -> str:
    """
    ФАКТОРИЗАЦИЯ В ВИДЕ СТРОКИ.
    Пример: factorize_str(4146) → "2 × 3 × 691"
    """
    if n == 0:
        return "0"
    if n < 0:
        return "-" + factorize_str(-n)
    
    factors: Dict[int, int] = {}
    temp = n
    d = 2
    while d * d <= temp:
        while temp % d == 0:
            factors[d] = factors.get(d, 0) + 1
            temp //= d
        d += 1 if d == 2 else 2
    if temp > 1:
        factors[temp] = factors.get(temp, 0) + 1
    
    if not factors:
        return "1"
    
    parts = [f"{p}^{e}" if e > 1 else str(p) for p, e in sorted(factors.items())]
    return " × ".join(parts)


