def classify_mod24_classes() -> Dict[int, Dict]:
    """
    КЛАССИФИКАЦИЯ ВСЕХ 24 КЛАССОВ ВЫЧЕТОВ ПО МОДУЛЮ 24.
    
    Для каждого r ∈ {0..23} вычисляем:
    • f(r) mod 24
    • g = gcd(f(r) mod 24, 24)
    • d = 24 / g
    
    Если f(r) ≡ 0 (mod 24): класс «GOOD», M(k) целое, n(k)/2 = k · M(k).
    Иначе: класс «BAD», n(k)/2 = (k/d) · f(k)/g.
    """
    classes = {}
    for r in range(24):
        f_r_mod24 = f_poly(r) % 24
        g = math.gcd(f_r_mod24, 24)
        d = 24 // g
        
        if f_r_mod24 == 0:
            class_type = "GOOD"
            factorization = "n(k)/2 = k · M(k)"
        else:
            class_type = "BAD"
            factorization = f"n(k)/2 = (k/{d}) · f(k)/{g}"
        
        classes[r] = {
            'r': r,
            'f_r_mod24': f_r_mod24,
            'gcd': g,
            'd': d,
            'type': class_type,
            'factorization': factorization,
            'threshold': d + 1,  # k > d ⇒ первый сомножитель > 1
        }
    return classes


